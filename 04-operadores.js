/* operadores  aritmeticos, sirven para hacer operaciones
+ Sirve para sumar
- Sirve para restar
* Sirve para multiplicar
% modulo no sirve para obtener el residuo de una división
/ division nos sirve para dividir dos numeros
*/

const numero1 = parseInt(prompt('Ingresa el número 1 '));
const numero2 = parseInt(prompt('Ingresa el número 1 '));
const suma = numero1 + numero2;
const resta = numero1 - numero2;
const multiplicacion = numero1 * numero2;
const division = numero1 / numero2; 
console.log('Suma:' + suma);
console.log('Resta:' +resta);
console.log('Multiplicacion:' +multiplicacion);
console.log('Division:' +division);
